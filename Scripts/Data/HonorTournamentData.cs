namespace UsurperRemake.Data;

/// <summary>
/// The Tournament of Honor -- v0.62.x "Light and Dark" Phase 7 (slice 6b). A short, focused
/// three-fight ritual hosted at The Sanctum and gated on Defender+ Renown standing (Chivalry
/// >= 250). The Light-pole mirror of Anchor Road's Gauntlet, but DELIBERATELY shorter and
/// more ceremonial: where the Gauntlet is endurance against 10 waves, the Tournament is a
/// focused trial against three named foes that a Light hero would specifically be called
/// to confront -- a fallen knight (a brother betrayed), an undead lord (the desecrated dead),
/// and the Anonymous Champion (the reigning ritual winner whose name is never spoken).
///
/// Shares infrastructure with the Gauntlet:
/// - `GauntletChampionData.ArenaTier` enum + `GetTierForLevel` (tier earned by player level on full clear)
/// - `Character.ArenaChampionTier` save field (highest tier reached across either tournament)
/// - `Character.GauntletRunsToday` daily counter (so it's Tournament OR Gauntlet today, not both)
/// - `Character.PFights` per-day combat-slot counter
///
/// Phase 7 first-slice scope deliberately constrained: 3 champions, no warmup waves, English
/// fields drive the source-of-truth with `tournament.champ.{id}.*` loc keys overriding for
/// localized sessions. Slice 7b can add additional champions, the cosmetic title-flavor split
/// (Honorbound vs Hopeful at lower tiers), and the warmup-wave structure if the loop proves
/// to want more length.
/// </summary>
public static class HonorTournamentData
{
    public enum HonorChampionRole
    {
        /// <summary>Fallen knight -- mirror of a Paladin who lost their faith. Heavy melee, paladin gear, broken combat style.</summary>
        FallenPaladin,
        /// <summary>Undead lord -- dark magic, bone-summons, drain. The desecrated dead.</summary>
        UndeadLord,
        /// <summary>The Anonymous Champion -- the reigning ritual winner. Multi-phase, balanced threat. Never speaks.</summary>
        AnonymousChampion
    }

    public class HonorChampion
    {
        public string Id { get; init; } = "";
        public string Name { get; init; } = "";
        public string Title { get; init; } = "";
        public HonorChampionRole Role { get; init; }

        /// <summary>HP multiplier on base scaled stats. Modest -- Tournament fights are shorter than Gauntlet so
        /// HP can be higher per-fight without making the whole loop tedious.</summary>
        public float HpMultiplier { get; init; }
        public float AttackMultiplier { get; init; }
        public float DefenseMultiplier { get; init; }

        /// <summary>Player-level offset for this champion. Tournament champions scale +3/+5/+7 across the three
        /// slots (vs Gauntlet's +2..+14 spread) -- the gap is tighter because there are fewer fights.</summary>
        public int LevelBonus { get; init; }

        /// <summary>The herald's announcement before combat. 2-3 lines. May reference {0} for the player's display name.</summary>
        public string[] EntranceAnnouncement { get; init; } = System.Array.Empty<string>();

        /// <summary>One-line lore reveal printed in italics after the entrance.</summary>
        public string LoreLine { get; init; } = "";

        /// <summary>What the crowd / hall does when this champion walks out.</summary>
        public string CrowdReaction { get; init; } = "";

        /// <summary>Themed equipment drop on defeat -- runtime stats are generated by the tournament
        /// method using LootGenerator scaled to player level.</summary>
        public ChampionDrop Drop { get; init; } = new();

        /// <summary>Monster abilities the champion uses. Picked at random per round by the existing
        /// MonsterAbilities pipeline. Names must match valid AbilityType enum entries.</summary>
        public string[] SpecialAbilities { get; init; } = System.Array.Empty<string>();

        // Localized accessors. English fields are the source/fallback; localized keys override.
        private static string LocOr(string key, string fallback)
        {
            var v = UsurperRemake.Systems.Loc.Get(key);
            return v == key ? fallback : v;
        }
        public string[] LocEntrance()
        {
            var r = new string[EntranceAnnouncement.Length];
            for (int i = 0; i < r.Length; i++)
                r[i] = LocOr($"tournament.champ.{Id}.entrance.{i}", EntranceAnnouncement[i]);
            return r;
        }
        public string LocLore() => LocOr($"tournament.champ.{Id}.lore", LoreLine);
        public string LocCrowd() => LocOr($"tournament.champ.{Id}.crowd", CrowdReaction);
        public string LocDropFlavor() => LocOr($"tournament.champ.{Id}.drop_flavor", Drop.FlavorDescription);
        public string LocName() => LocOr($"tournament.champ.{Id}.name", Name);
    }

