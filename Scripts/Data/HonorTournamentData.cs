    /// <summary>
    /// Reward tuning for a full Tournament clear. Multiplies by player level on victory.
    /// Tighter spread than Gauntlet's (which goes 200..1500 gold/level across 5 tiers)
    /// because the Tournament is a shorter ritual -- gold per minute is comparable but the
    /// per-clear payout sits in the upper-mid range. Fame bonus matches Gauntlet's so a
    /// full clear contributes equally to knighthood eligibility.
    ///
    /// Chivalry is awarded PER WAVE (10/15/20 across the three waves) inside the tournament
    /// loop via AlignmentSystem.ChangeAlignment, so the v0.60.0 DR curve fires and a Hero+
    /// player still gets a meaningful but modest climb. NOT applied here in the per-tier
    /// summary because tier-clears already trigger a tier-up message.
    /// </summary>
    public class TierRewards
    {
        public int GoldMultiplierPerLevel { get; init; }
        public int XpMultiplierPerLevel { get; init; }
        public int FameBonus { get; init; }
    }

    public static TierRewards GetTierRewards(GauntletChampionData.ArenaTier tier) => tier switch
    {
        GauntletChampionData.ArenaTier.Hopeful       => new TierRewards { GoldMultiplierPerLevel =  250, XpMultiplierPerLevel = 130, FameBonus =  35 },
        GauntletChampionData.ArenaTier.Veteran       => new TierRewards { GoldMultiplierPerLevel =  500, XpMultiplierPerLevel = 250, FameBonus =  60 },
        GauntletChampionData.ArenaTier.Master        => new TierRewards { GoldMultiplierPerLevel =  850, XpMultiplierPerLevel = 420, FameBonus =  90 },
        GauntletChampionData.ArenaTier.Champion      => new TierRewards { GoldMultiplierPerLevel = 1300, XpMultiplierPerLevel = 600, FameBonus = 120 },
        GauntletChampionData.ArenaTier.GrandChampion => new TierRewards { GoldMultiplierPerLevel = 1700, XpMultiplierPerLevel = 820, FameBonus = 175 },
        _ => new TierRewards()
    };
}
